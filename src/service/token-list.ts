const parseList = (list: IRaw[]) => list.map(([value, count]) => {
  if (!count) {
    return value;
  }
  let result = '';
  const resultArray: string[] = [];
  for (let i = 0; i < count; i++) {
    result += value;
    resultArray.push(result);
  }

  return resultArray;
}).flat();

